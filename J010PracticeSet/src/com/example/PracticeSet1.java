package com.example;

public class PracticeSet1 {

	public static void main(String[] args) {
		
		char grade = 'B';
		
		//type conversion
		grade = (char)(grade + 8);
		
		System.out.println(grade);
		
		// decrypting
		grade = (char)(grade - 8);
		System.out.println(grade);

	}

}
